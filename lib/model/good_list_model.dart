




class BannerModel{
  int code;
  String msg;
  List<BannerBean> data;

  BannerModel(this.data);

  BannerModel.fromJson(Map<String,dynamic> json){
    code = json['code'];
    msg = json['msg'];
    if(json['data'] != null){
      data = List<BannerBean>();
      json['data'].forEach((v){
        data.add(v);
      });
    }
  }
}
